const fs = require('fs');

// Создаем простую PNG иконку для трея (32x32)
// Это базовый PNG файл с прозрачным фоном и простой иконкой
const createSimpleIcon = () => {
  // Простой PNG файл 32x32 с прозрачным фоном
  // PNG заголовок + IHDR + IDAT + IEND
  const pngData = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    // IHDR chunk
    0x00, 0x00, 0x00, 0x0D, // Length
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x20, // Width: 32
    0x00, 0x00, 0x00, 0x20, // Height: 32
    0x08, 0x06, 0x00, 0x00, 0x00, // Bit depth, color type, compression, filter, interlace
    0x73, 0x6A, 0xCC, 0x40, // CRC
    // IDAT chunk (минимальные данные)
    0x00, 0x00, 0x00, 0x0C, // Length
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, // Compressed data
    0xE2, 0x21, 0xBC, 0x33, // CRC
    // IEND chunk
    0x00, 0x00, 0x00, 0x00, // Length
    0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82  // CRC
  ]);
  
  return pngData;
};

// Создаем иконку
const iconData = createSimpleIcon();
fs.writeFileSync('./assets/tray-icon.png', iconData);
fs.writeFileSync('./assets/icon.png', iconData);

console.log('✅ Простые иконки созданы');

